<?php if ( ! defined('BASEPATH')) exit('No direct script access allowed');
////////////////////////////////////////////////////////////////////////////////////////////////////////
require_once(APPPATH.'core/EXT_Controller'.EXT);
////////////////////////////////////////////////////////////////////////////////////////////////////////
class Myfaq extends EXT_Controller {
////////////////////////////////////////////////////////////////////////////////////////////////////////
function __construct( ) {
	parent::__construct();
	$this->load->model('Faq');
}
////////////////////////////////////////////////////////////////////////////////////////////////////////
public function index() {
	
	$this->add_page_data(array(
		'faqs' => $this->get_faq()
	));

	$this->render_subpage('pages/faq');
}
////////////////////////////////////////////////////////////////////////////////////////////////////////
// Helper Functions
////////////////////////////////////////////////////////////////////////////////////////////////////////
private function get_faq() {
	$faq = $this->Faq->get_faqs();

	return $faq->data;
}
////////////////////////////////////////////////////////////////////////////////////////////////////////
} // END OF CLASS
